<h1>'v-bind' Example</h1>

<p>The browser finds the class name inside the Vue instance.</p>

<div id="app">
  <p>Everything inside the div emelent with id="app" can use data from the Vue instance.</p>
  <div v-bind:class="vueClass">This element is bound to the "pinkBG" class.</div>

</div>

<script src="https://unpkg.com/vue@3/dist/vue.global.js"></script>

<script>
  const app = Vue.createApp({
   data() {
    return {
     vueClass: "pinkBG"
    }
   }
  })
  
  app.mount('#app')

</script>

